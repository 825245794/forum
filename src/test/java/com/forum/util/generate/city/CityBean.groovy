package com.forum.util.generate.city

class CityBean {
    public int maxid;
    public int id;
    public String name;

    int getMaxid() {
        return maxid
    }

    void setMaxid(int maxid) {
        this.maxid = maxid
    }

    int getId() {
        return id
    }

    void setId(int id) {
        this.id = id
    }

    String getName() {
        return name
    }

    void setName(String name) {
        this.name = name
    }
}
